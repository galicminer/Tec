%ejercicio3
t=0:0.1:2*pi;
u=0:0.1:2*pi;
a=1;
b=1;
ax=@(t,u)(a.*cos(t).*cos(u));
x=ax(t,u)
ay=@(t,u)(b.*cos(t).*sin(u));
y=ay(t,u)
az=@(t,u)(cos(t).^2);
z=az(t,u)
plot3(x,y,z)
